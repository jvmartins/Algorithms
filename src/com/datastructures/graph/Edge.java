package com.datastructures.graph;


public class Edge {
	
	DirectedVertex from;
	DirectedVertex to;
	
	public DirectedVertex getFrom() {
		return from;
	}
	public void setFrom(DirectedVertex from) {
		this.from = from;
	}
	public DirectedVertex getTo() {
		return to;
	}
	public void setTo(DirectedVertex to) {
		this.to = to;
	}
	
}
